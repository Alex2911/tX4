		function resetContent(menu)
		{
			$$('#' + menu + ' ul').addClass('hide');

			$$('#' + menu + ' .container').removeClass('active');
			$$('#' + menu + ' .container').addClass('normal');
		}

		function showTroops(vid)
		{
			resetContent('troops');
			$$('#troops .troops' + vid).removeClass('hide');
			$$('#troops .contentNavi .vid' + vid).removeClass('normal');
			$$('#troops .contentNavi .vid' + vid).addClass('active');
		}

		function showBuildings(category)
		{
			resetContent('buildings');
			$$('#buildings .buildings' + category).removeClass('hide');
			$$('#buildings .contentNavi .category' + category).removeClass('normal');
			$$('#buildings .contentNavi .category' + category).addClass('active');
		}
